#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*c;
	int		i;
	int		a;

	i = 0;
	a = n;
	c = s;
	while (i < a)
	{
		c[i] = '\0';
		i ++;
	}
}
